
public class Ders {
	private String dersAdi;
	private int dersKredi;
	private int dersNotu;
	
		
	public Ders() {
		super();
	}
	public Ders(String dersAdi) {
		super();
		this.dersAdi = dersAdi;
	}
	
	public String getDersAdi() {
		return dersAdi;
	}
	public void setDersAdi(String dersAdi) {
		this.dersAdi = dersAdi;
	}
	public int getDersKredi() {
		return dersKredi;
	}
	public void setDersKredi(int dersKredi) {
		this.dersKredi = dersKredi;
	}
	public int getDersNotu() {
		return dersNotu;
	}
	public void setDersNotu(int dersNotu) {
		this.dersNotu = dersNotu;
	}
	

}
